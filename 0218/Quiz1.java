package dataType;

public class Quiz1 {
	public static void main(String[] args) {
		String name = "서호준";
		//정수형 변수 age를 선언하면서 자신의 나이로 초기화 하세요
		
		//실수형 변수 height를 선언하면서 자신의 키로 초기화하세요(소수점 포함)
		
		//000 님의 나이는 00 살이고, 키는 000.xx cm 입니다. 라고 출력
		int age = 25;
		double height = 167.00;
		System.out.println(name + "님의 나이는 " + age + "살 이고, 키는 " + height + "cm 입니다.");
		//System.out.println(); -> 자동 1줄 바꿈
		
	}
}
